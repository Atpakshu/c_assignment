/*Print the ASCII value of user entered character in decimal, hex, octal format and also print the
character for user entered ASCII value*/


#include<stdio.h>
int main()
{

char ch;

printf("Enter the character : %c",ch);
scanf("%c",&ch);

printf("characters ASCII value(Decimal) : %d \n",ch);

printf("characters ASCII value in hexadecimal : %x \n",ch);

printf("characters ASCII value in octal format : %o \n",ch);

return 0;




}


































